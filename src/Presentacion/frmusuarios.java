    void llenacombo() {  //declara function que se llama en el initcomponents
        ftusuario func = new ftusuario(); //instanciar o llamar a la clase de funciones usuario
        
        ArrayList<vpersona> personas = func.getPersonas();  //declarar una variable personas de tipo array de strings y asigna resultado de la funcion getPersonas
        if (personas.isEmpty()) { //validar si no esta vacia
            System.out.println("Error on llenacombo return personas null");
        }
        Vector<vpersona> items =new Vector<>();
        for (vpersona persona : personas) {
            items.add(persona);
        }
          comboModelo =  new DefaultComboBoxModel(items);
          cbopersona.setModel(comboModelo);
    }

