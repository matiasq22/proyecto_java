    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        
          if(!txtidusuario.getText().equals("")){
            int confirmacion=JOptionPane.showConfirmDialog(rootPane, "Esta seguro de Eliminar el Usuario","Confirmar",2);
            
            if(confirmacion==0){
                ftusuario func=new ftusuario();
                vusuario dts = new vusuario();
                dts.setidusuario(Integer.parseInt(txtidusuario.getText()));
                func.eliminar(dts);
                mostrar("");
                //inhabilitar();
            }
            
        }
        
        
    }//GEN-LAST:event_btneliminarActionPerformed

